public class LuasLingkaran {

    public static void main(String[] args) {

            
            int r;
            double pi, luas_lingkaran;


            
            pi= 3.14;
            r=18;
            luas_lingkaran= pi*r*r;

            
            System.out.println("Diketahui pi sebuah lingkaran adalah " + pi + " cm");
            System.out.println("Jari-jari lingkaran adalah " + r + " cm");
            System.out.println("Maka, luas lingkaran dengan jari-jari 18 adalah " + luas_lingkaran + " cm");

    }
}
